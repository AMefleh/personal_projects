from turtle import *

setup(650, 700)

#first letter; A (starting with default pen color and 10 for pen size)
pensize(10)
penup()
goto(-270, -150)
pendown()
goto(-210, 150)
goto(-150, -150)
penup()
goto(-240, 0)
pendown()
goto(-180, 0)

#second letter; P (changing to maroon pen color)
pencolor("maroon")
penup()
goto(-135, -150)
pendown()
goto(-135, 150)
goto(-45, 150)
goto(-15, 100)
goto(-15, 50)
goto(-45, 0)
goto(-135, 0)

#third letter; P (changing to navy pen color with everything shifted 135 along x-axis)
pencolor("navy")
penup()
goto(0, -150)
pendown()
goto(0, 150)
goto(90, 150)
goto(120, 100)
goto(120, 50)
goto(90, 0)
goto(0, 0)

#fourth letter, S (changing to gold pen color)
pencolor("gold")
penup()
goto(135, -150)
pendown()
goto(225, -150)
goto(255, -100)
goto(255, -50)
goto(225, 0)
goto(165, 0)
goto(135, 50)
goto(135, 100)
goto(165, 150)
goto(255, 150)

#underlining letters #1 with different colors and width
penup()
goto(-270, -180)
pendown()
pensize(15)
pencolor("gold")
forward(135)
pencolor("navy")
forward(135)
pencolor("maroon")
forward(135)
pencolor("black")
forward(120)

#underlining letters #2 with different colors and width
penup()
goto(-240, -210)
pendown()
pensize(10)
pencolor("gold")
forward(120)
pencolor("navy")
forward(120)
pencolor("maroon")
forward(120)
pencolor("black")
forward(90)

#underlining letters #2 with different colors and width
penup()
goto(-210, -240)
pendown()
pensize(5)
pencolor("gold")
forward(105)
pencolor("navy")
forward(105)
pencolor("maroon")
forward(105)
pencolor("black")
forward(60)

#line above word #1 - similar to previous block
penup()
goto(-270, 180)
pendown()
pensize(15)
pencolor("gold")
forward(135)
pencolor("navy")
forward(135)
pencolor("maroon")
forward(135)
pencolor("black")
forward(120)

#line above word #2 - similar to previous block
penup()
goto(-240, 210)
pendown()
pensize(10)
pencolor("gold")
forward(120)
pencolor("navy")
forward(120)
pencolor("maroon")
forward(120)
pencolor("black")
forward(90)

#line above word #3 - similar to previous block
penup()
goto(-210, 240)
pendown()
pensize(5)
pencolor("gold")
forward(105)
pencolor("navy")
forward(105)
pencolor("maroon")
forward(105)
pencolor("black")
forward(60)

exitonclick()
